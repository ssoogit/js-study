// ──────────────────────────────
// null 병합 연산자
// ──────────────────────────────
let num1;
let num2 = 10;

console.log(num1 ?? 20); // 20 출력
console.log(num2 ?? 20); // 10 출력

// ──────────────────────────────
// 삼항 연산자
// ──────────────────────────────
let num = 100;
let result = num % 2 === 0 ? "짝수" : "홀수"

console.log(result); // 짝수 출력
